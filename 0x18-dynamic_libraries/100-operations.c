#include <stdio.h>
#include "main.h"
/**
 * *add - adds 2 numbers
 * *sub - subtract 2 numbers
 * *mul - multiply 2 numbers
 * *div - divides 2 numbers
 * *mod - divides 2 numbers and returns its remainder
 * *@a: int type number
 * *@b: int type number
 * *Return : results of calculations
 * */
int add(int a, int b)
{
	return (a + b);
}
int sub(int a, int b)
{
	return (a - b);
}
int mul(int a, int b)
{
	return (a * b);
}
int  div(int  a, int b)
{
	return (a / b);
}
int mod(int a, int b)
{
	return (a % b);
}

